package Aula34;

public class ConversaoDeUnidadeDeArea {
	
	public ConversaoDeUnidadeDeArea() {}
	
	public static double MetrosParaPés(double metro) {
	
		return (metro*10.76);
	}
	public static double PésParaCent(double pe) {
		
		return (pe*929);
	}
	public static double MilhaParaAcre(double milha) {
		
		return (milha*640);
	}
	public static double AcreParaPés(double acre) {
		
		return (acre*43560);
	}

}
